package codescripters.news.borders.Objects;

public class NewsObject {

	String id;
	String title;
	String url;
	String source;
	String title_full;
	String body_full;
	String media_type;
	String media_src;
	String publishedOn;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getTitle_full() {
		return title_full;
	}

	public void setTitle_full(String title_full) {
		this.title_full = title_full;
	}

	public String getBody_full() {
		return body_full;
	}

	public void setBody_full(String body_full) {
		this.body_full = body_full;
	}

	public String getMedia_type() {
		return media_type;
	}

	public void setMedia_type(String media_type) {
		this.media_type = media_type;
	}

	public String getMedia_src() {
		return media_src;
	}

	public void setMedia_src(String media_src) {
		this.media_src = media_src;
	}

	public String getPublishedOn() {
		return publishedOn;
	}

	public void setPublishedOn(String publishedOn) {
		this.publishedOn = publishedOn;
	}

}
